        /// <param name="defValue">缺省值</param>
        /// <returns>转换后的int类型结果</returns>
        public static decimal StrToDecimal(string strValue, decimal defValue)
        {
            if ((strValue == null) || (strValue.Length > 18))
                return defValue;

            decimal intValue = defValue;
            if (strValue != null)
            {
                bool IsDecimal = Regex.IsMatch(strValue, @"^([-]|[0-9])[0-9]*(\.\w*)?$");
                if (IsDecimal)
                    intValue = decimal.Parse(strValue);
            }
            return intValue;
        }


        /// <summary>
        /// int型转换为bool型 0,-1为false,其它为true
        /// </summary>
        /// <param name="expression">要转换的字符串</param>
        /// <param name="defValue">缺省值</param>
        /// <returns>转换后的bool类型结果</returns>
        public static bool intToBool(int expression, bool defValue)
        {
            if (expression == 0 || expression == -1)
            {
                return false;
            }

            return true;
        }

